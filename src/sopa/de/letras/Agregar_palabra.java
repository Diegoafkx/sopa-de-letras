    /**
     * metodo que se llama despues de hacer click en el boton jButton1
     * Este metodo se encara de leer la palabra escrita por el usuario hacer verificaciones y meterla en el diccionario.
     * @param evt 
     */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String x = this.New_palabras.getText().toUpperCase(); 
        boolean aux = true;
        for (int i = 0; i < menu.getlist_p().Tamaño(); i++) {
            if(menu.getlist_p().Datos(i).equals(x)){
                this.Aviso.setText("Ya se encuentra en el diccionario la palabra.");
                aux = false;
            }
        }
        if(aux == true ){
                if(x.length() < 3 || x.length() >4){
                    if (x.equals("")){
                    this.Aviso.setText("Error. No se ha escribio ninguna palabra");
                }else{
                this.Aviso.setText("Error. Tamaño de la palabra es invalida");
                }
            }else{
                menu.getlist_p().Insertar(x);
                menu.ag();
            }
        }
        this.New_palabras.setText(""); 
    }//GEN-LAST:event_jButton1ActionPerformed
    
